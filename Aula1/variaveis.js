console.log("=== VARIÁVEIS ===");

/*VARIAVEIS
Espaço reservado na memória para guardar uma informação
Todas as variáveis são prefixadas com as seguintes palavra-chave: var, let e const

Sintaxe:

var chave = "valor";
let chave = "valor";
const chave = "valor";
*/

var nome = "Rodrigo";
console.log("nome", nome, typeof nome);

let idade = "30";
console.log("idade", idade, typeof idade);

const alto = true;
console.log("alto", alto, typeof alto);

var nulo = null;
console.log("nulo", nulo, typeof nulo)

var indefinida = undefined;
console.log("undefined", indefinida, typeof indefinida);



